namespace HgCo.WindowsLive.SkyDrive
{
    /// <summary>
    /// Specifies the category type of a webfolder.
    /// </summary>
    public enum WebFolderCategoryType
    {
        /// <summary>
        /// The specified webfolder's category type cannot be determined.
        /// </summary>
        None,
        /// <summary>
        /// The specified webfolder contains documents.
        /// </summary>
        Documents,
        /// <summary>
        /// The specified webfolder contains favorites.
        /// </summary>
        Favorites,
        /// <summary>
        /// The specified webfolder contains photos.
        /// </summary>
        Photos
    }
}